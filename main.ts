sprites.onOverlap(SpriteKind.Player, SpriteKind.Enemy, function (sprite, otherSprite) {
    otherSprite.destroy(effects.disintegrate, 500)
    if (sprite.y < otherSprite.y) {
        info.changeScoreBy(3)
        sprite.vy = -150
    } else {
        info.changeLifeBy(-1)
    }
})
